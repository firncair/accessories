#pragma once

int64_t timediff(struct timespec t1, struct timespec t2)
{
    int64_t i64T1=(t1.tv_sec * 1000000000) + t1.tv_nsec;
    int64_t i64T2=(t2.tv_sec * 1000000000) + t2.tv_nsec;

    return (i64T1 - i64T2);
}

int64_t timediffms(struct timespec t1, struct timespec t2)
{
    return (timediff(t1, t2) / 1000000);
}
